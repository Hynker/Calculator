/**
 * Функция вычисления суммы чисел
 * @param {Number} x - Первое слагаемое
 * @param {Number} y - Второе слагаемое
 * @returns Сумма x и y
 */
function add(x, y) {
    return x + y;
}

/**
 * Функция вычисления разности чисел
 * @param {Number} x - Уменьшаемое
 * @param {Number} y - Вычитаемое
 * @returns Разность x и y
 */
function sub(x, y) {
    return x - y;
}

/**
 * Функция вычисления произведения чисел
 * @param {Number} x - Первый множитель
 * @param {Number} y - Второй множитель
 * @returns Произведение x на y
 */
function mul(x, y) {
    return x * y;
}

/**
 * Функция вычисления произведения чисел
 * @param {Number} x - Первый множитель
 * @param {Number} y - Второй множитель
 * @returns Произведение x на y
 */
function div(x, y) {
    return x / y;
}

/**
 * Функция возведения числа в степень
 * @param {Number} x - Основание степени
 * @param {Number} y - Показатель степени
 * @returns Значение x в степени y
 */
function pow(x, y) {
        return Math.pow(x,y);
}

/**
 * Функция вычисления квадратного корня
 * @param {Number} x - Подкоренное выражение (число)
 * @returns Квадратный корень из x
 */
function sqrt(x) {
    return Math.sqrt(x);
}